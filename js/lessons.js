function validateLessonContent(idx,arr){
  const wl=window.LESSON_WHITELISTS[idx];if(!wl)return arr;
  const isDiac=cp=>(cp>=0x064B&&cp<=0x065F)||cp===0x0670;
  return arr.filter(e=>{for(const ch of e){const cp=ch.codePointAt(0);if(isDiac(cp)||wl.has(ch))continue;return false;}return true;});
}
