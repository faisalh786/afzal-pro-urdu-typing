const STORAGE_KEY='afzal-pro-data';
const CAND_KEY='afzal-pro-candidate';
function loadData(){try{return JSON.parse(localStorage.getItem(STORAGE_KEY))||{sessions:[],completedLessons:{},bestWPM:0,totalChars:0,totalSessions:0,streak:0,lastDate:''};}catch(e){return{sessions:[],completedLessons:{},bestWPM:0,totalChars:0,totalSessions:0,streak:0,lastDate:''};}}
function saveData(d){try{localStorage.setItem(STORAGE_KEY,JSON.stringify(d));}catch(e){}}
function getCandidateName(){return document.getElementById('candidate-name-input')?.value?.trim()||'—';}
function saveCandidateNameLive(){localStorage.setItem(CAND_KEY,document.getElementById('candidate-name-input')?.value||'');}
function saveSession(type,lessonId,wpm,acc,chars){
  const d=loadData();const today=new Date().toISOString().slice(0,10);
  if(d.lastDate!==today){d.streak=(d.lastDate===getPrevDay(today)?d.streak+1:1);d.lastDate=today;}
  d.sessions.push({date:new Date().toLocaleString(),type,lessonId,wpm,acc,chars,grade:calcGrade(wpm,acc)});
  if(d.sessions.length>200)d.sessions=d.sessions.slice(-200);
  if(wpm>d.bestWPM)d.bestWPM=wpm;d.totalChars+=chars;d.totalSessions++;
  if(type==='lesson'&&lessonId!==undefined){if(!d.completedLessons[lessonId]||wpm>(d.completedLessons[lessonId].wpm||0))d.completedLessons[lessonId]={wpm,acc,grade:calcGrade(wpm,acc)};}
  saveData(d);document.getElementById('streak-val').textContent=d.streak||0;
}
function getPrevDay(ds){const d=new Date(ds);d.setDate(d.getDate()-1);return d.toISOString().slice(0,10);}
function calcGrade(wpm,acc){if(wpm>=40&&acc>=95)return'A';if(wpm>=30&&acc>=90)return'B';if(wpm>=20&&acc>=80)return'C';return'D';}

